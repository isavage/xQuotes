package vrs.eulogia.xquotes;

public class Category {
	
	private int _id;
	private String category;
	private int cat_allowed;
	
	public int get_id() {
		return _id;
	}
	public void set_id(int _id) {
		this._id = _id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getCat_allowed() {
		return cat_allowed;
	}
	public void setCat_allowed(int cat_allowed) {
		this.cat_allowed = cat_allowed;
	}
	
	
}
