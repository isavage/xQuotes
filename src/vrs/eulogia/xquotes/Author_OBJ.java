package vrs.eulogia.xquotes;

public class Author_OBJ {
	
	private String author;
	private String img;

public Author_OBJ(String author, String img){
	this.author = author;
	this.img = img;
}

public String getAuthor() {
	return author;
}

public void setAuthor(String author) {
	this.author = author;
}

public String getImg() {
	return img;
}

public void setImg(String img) {
	this.img = img;
}


}